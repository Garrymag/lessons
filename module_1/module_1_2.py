example = 'Топинамбур'  # Присваиваем переменной example строку "Топинамбур"

print(example[0])  # Выводим первый символ строки (индекс 0)
print(example[-1]) # Выводим последний символ строки (индекс -1)
print(example[len(example) // 2:]) # Выводим вторую половину строки (средину округляем вниз)
print(example[::-1]) # Выводим строку в обратном порядке
print(example[1::2]) # Выводим каждый второй символ строки (с шагом 2 начиная с индекса 1)