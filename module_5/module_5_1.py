# Создаем класс House для представления многоэтажного дома
class House:
    # Метод инициализации объекта класса
    def __init__(self, name, number_of_floors):
        # Сохраняем название дома как атрибут объекта
        self.name = name

        # Сохраняем количество этажей как атрибут объекта
        self.number_of_floors = number_of_floors

    # Метод перемещения по этажам дома
    def go_to(self, new_floor):
        # Проверяем, является ли указанный этаж корректным
        # Если этаж меньше 1 или больше максимального количества этажей
        if new_floor < 1 or new_floor > self.number_of_floors:
            # Выводим сообщение о некорректном этаже
            print("Такого этажа не существует")
        else:
            # Если этаж корректный, выводим все этажи от 1 до указанного
            # range(1, new_floor + 1) создает последовательность от 1 до new_floor включительно
            for floor in range(1, new_floor + 1):
                # Выводим номер каждого этажа
                print(floor)


# Создаем первый объект дома - многоэтажный жилой комплекс
h1 = House('ЖК Горский', 18)

# Создаем второй объект дома - небольшой дом
h2 = House('Домик в деревне', 2)

# Демонстрация работы метода go_to для первого дома
# Попытка перейти на 5 этаж (корректный сценарий)
print("Переход на 5 этаж ЖК Горский:")
h1.go_to(5)

# Демонстрация работы метода go_to для второго дома
# Попытка перейти на 10 этаж (некорректный сценарий)
print("\nПопытка перехода на 10 этаж Домика в деревне:")
h2.go_to(10)